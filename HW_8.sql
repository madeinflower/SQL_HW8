 -- 1. Определить какие из покупок были совершены в июне
 
SELECT * 
FROM ORDERS 
WHERE MONTH(ODATE) = 6 
AND YEAR(ODATE) = 2022;

-- 2. Определить какие из покупок были совершены в интервале от 10 апреля 2022 до 10 мая 2022 года

SELECT * 
FROM ORDERS 
WHERE ODATE BETWEEN '2022-04-10' AND '2022-05-10';

-- 3. Определить сколько покупок было совершено в марте

SELECT COUNT(*) AS NumberOfPurchases 
FROM ORDERS 
WHERE MONTH(ODATE) = 3 
AND YEAR(ODATE) = 2022;

-- 4. Определить среднюю стоимость покупок в марте

SELECT AVG(AMT) AS AveragePurchaseAmount 
FROM ORDERS 
WHERE MONTH(ODATE) = 3 
AND YEAR(ODATE) = 2022;

-- 5. Выведите минимальный рейтинг покупателя среди сделавших заказ в апреле 2022 года

SELECT MIN(C.RATING) AS MinimumRating 
FROM ORDERS O
JOIN CUSTOMERS C ON O.CUST_ID = C.CUST_ID
WHERE MONTH(O.ODATE) = 4 
AND YEAR(O.ODATE) = 2022;

-- 6. Выведите продавцов, которые оформили заказ на наибольшую сумму в период с 1 марта по 1 май 2022 года

SELECT SELL_ID, SUM(AMT) AS TotalAmount 
FROM ORDERS 
WHERE ODATE BETWEEN '2022-03-01' AND '2022-05-01'
GROUP BY SELL_ID
ORDER BY TotalAmount DESC
LIMIT 1;
